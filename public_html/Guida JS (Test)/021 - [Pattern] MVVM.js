
// Il pattern MVVM ( Model - View - ViewModel ) è una variante del pattern
// MVC in cui la View ha un ruolo più "attivo" nel senso che si occupa della gestione di eventi 
// ed eseguire alcune operazioni.
// 
// Il ViewModel invece è un'estensione del Model che si occupa di manipolare i dati del model
// e dell'interazione con la view.
// 
// Praticamente è come se il cotroller fosse diviso in due, una parte di funzionalità più vicine alla View inglobate in essa
// e una parte di funzionalità più vicine al model inserite in questo nuovo "componente": il ViewModel.



